"""
Operadores de Comparação / Relacionais

maior >
maior ou igual >=
menor <
menor ou igual <=
igual ==
diferente !=
"""

maior = 10 > 5
maior_ou_igual = 10 >= 5

menor = 5 < 10
menor_ou_igual = 5 <= 10

igual = 10 == 10
diferente = 10 != 5

print(maior, maior_ou_igual, menor, menor_ou_igual, igual, diferente)
