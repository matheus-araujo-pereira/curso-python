"""
Python é uma Linguagem de Programação

Possui tipagem dinâmica e forte

str -> string -> texto

Strings são textos que estão dentro das aspas
"""

# Aspas simples
print('Matheus Araujo Pereira')
print('Matheus "Araujo" Pereira')

# Aspas duplas
print("Matheus Araujo Pereira")
print("Matheus 'Araujo' Pereira")

# Escape
print("Matheus \"Araujo\" Pereira")

# r -> para exibir tudo
print(r"Matheus \"Araujo\" Pereira")