"""
Tipos int e float

int -> número inteiro

positivo ou negativo
"""
print(1)
print(-1)
print(0)

"""
float -> número com ponto flutuante

O tipo float representa qualquer número

positivo ou negativo
"""
print(1.1)
print(1.9)
print(1.0)
print(-1.5)
print(0.5)

"""
A função type mostra o tipo que o python inferiu ao valor
"""
print(type(1))
print(type(-1))
print(type(1.0))
print(type(-1.0))
print(type('Matheus'))
print(type("Matheus Araujo Pereira"))
print(type(1), type(1.0), type('Matheus'), type("Araujo Pereira"))